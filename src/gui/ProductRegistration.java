    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter product name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select brand", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String pname = jTextField1.getText();
            String bname = jComboBox1.getSelectedItem().toString();
            String cname = jComboBox2.getSelectedItem().toString();

            try {

                ResultSet rs = MySQL.search(("SELECT * FROM `product` JOIN `brand` ON product.brand_id=brand.id JOIN `category` ON product.category_id=category.id WHERE product.name='" + pname + "' AND brand.name='" + bname + "' AND category.name='" + cname + "'"));
//                System.out.println(rs);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Product already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    ResultSet rs1 = MySQL.search("SELECT `id` FROM `brand` WHERE `name` = '" + bname + "'");
                    rs1.next();
                    ResultSet rs2 = MySQL.search("SELECT `id` FROM `category` WHERE `name` = '" + cname + "'");
                    rs2.next();

                    String brand_id = rs1.getString("id");
                    String category_id = rs2.getString("id");

                    MySQL.iud("INSERT INTO `product`(`name`,`category_id`,`brand_id`) VALUES ('" + pname + "','" + category_id + "','" + brand_id + "')");

                    jTextField1.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    loadProduct();
                    JOptionPane.showMessageDialog(this, "New product added", "success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Pleasse select a Company", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String pid = jTable1.getValueAt(r, 0).toString();
                String pname = jTable1.getValueAt(r, 1).toString();
                String brand = jTable1.getValueAt(r, 2).toString();
                String category = jTable1.getValueAt(r, 3).toString();

                grn.jLabel10.setText(pid);
                grn.jLabel11.setText(pname);
                grn.jLabel14.setText(brand);
                grn.jLabel18.setText(category);
                grn.jTextField1.grabFocus();
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

