    public void loadProduct() {
        try {
            ResultSet rs = MySQL.search(("SELECT * FROM `product` JOIN `brand` ON product.brand_id=brand.id JOIN `category` ON product.category_id=category.id "));
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("category.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
        }
    }

