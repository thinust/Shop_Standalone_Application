    public void loadSupliers() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM supplier JOIN company_branch ON supplier.`company-branch_id`=company_branch.id JOIN company_branch_address ON company_branch_address.id=company_branch.company_branch_address_id JOIN city ON city.id=company_branch_address.city_id ORDER BY supplier.id ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("supplier.id"));
                v.add(rs.getString("supplier.name"));
                v.add(rs.getString("supplier.contact_number"));
                v.add(rs.getString("supplier.email"));
                v.add(rs.getString("company_branch.name"));
                v.add(rs.getString("company_branch.branch_contact_number"));
                String address = rs.getString("company_branch_address.line1") + "," + rs.getString("company_branch_address.line2") + "," + rs.getString("city.name");
                v.add(address);
                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

